using System;

namespace Calculadora
{
    public class CalculadoraSimples
    {
        // Adiciona dois números
        public int Adicionar (int n1, int n2)
        {
            return n1 + n2;
        }

        // Multiplica dois números
        public int Multiplicar(int n1, int n2)
        {
            // Demo:
            //return n1 + n2; 

            //Correção:
            return n1 * n2;
        }
    }
}
